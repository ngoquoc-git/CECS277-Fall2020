public class Point {
    // location at x - row
    public int x;
    // location at y - coluum
    public int y;

    /**
     * Overloaded COnstructor
     * pass location x, y to instance variables
     * @param x axis location
     * @param y axis location
     */
    public Point(int x, int y){
        this.x = x;
        this.y = y;
    }
}
